"""Strings: how to print single and double cotes in a string:...
 String is immutable (can't change)"""
print("Ganesh's Laptop")
print('Ganesh "Laptop"')
print('Ganesh\'s, "Laptop"') # \ skips the special meaning of the single(') codes
print("c:\docs\ngani")
print(r"c:\docs\ngani") # r - Raw string prints as it is...
name='Pycharm'
print(name[:]) # left side is inclusive : right side is exclusive..
number = (21,2,34,54,67)
